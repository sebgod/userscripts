:- import_module io.



:- pred main(io::di, io::uo) is det.

%------------------------------------------------------------------------------%
%------------------------------------------------------------------------------%

:- implementation.

:- import_module .

%------------------------------------------------------------------------------%

main -->

%------------------------------------------------------------------------------%
%------------------------------------------------------------------------------%
EOF
